
public class SwitchDemo {
public static void main(String[] args) {
	int a=20;
	switch (a) {
	case 1:
		System.out.println("value is 1");
		break;
	case 2:
		System.out.println("value is 2");
		break;
	case 10:
		System.out.println("value is 10");
		break;
	default:
		System.out.println("value doesnot exists");
		break;
	}
}
}
